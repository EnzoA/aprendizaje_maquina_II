def add_two_numbers(first_number: int, second_number: int) -> int:
    """
    This is a function to generate a pytest demo and
    returns the sum of both numbers passed as arguments.

    :param first_number: first number to add
    :type first_number: int

    :param second_number: second number to add
    :type second_number: int

    :return type: int
    """

    return first_number + second_number